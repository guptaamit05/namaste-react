export function generateRandomMessage() {
  const messageLength = Math.floor(Math.random() * 2) + 6; // Random length between 6 and 7
  let message = [];

  for (let i = 0; i < messageLength; i++) {
    message.push(getRandomWord());
  }

  return message.join(" ");
}


function isPrime(num) {
    if (num <= 1) {
      return false;
    }
    for (let i = 2; i <= Math.sqrt(num); i++) {
      if (num % i === 0) {
       return false;
      }
    }
    return true;
   }
  
   //Function to find the nth prime number
  export function findNthPrime(n) {
    let count = 0;
    let num = 2;
    while (count < n) {
      if (isPrime(num)) {
       count++;
      }
      num++;
    }
    return num - 1;
   }
  